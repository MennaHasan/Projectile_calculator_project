%this code is used to open one of the other tools in the GUI depending on the user's choice

function StartButtonPushed(app, event)
            val = app.chosetoolDropDown.Value;
            if val == "Tool 1"
                Tool1
            elseif val == "Tool 2"
                Tool2
            elseif val == "Tool 3"
                Tool3
            elseif val == "Tool 4"
                Tool4
            elseif val == "Tool 5"
                Tool5
            end
           
end
